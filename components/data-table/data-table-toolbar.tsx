"use client";

import { Table } from "@tanstack/react-table";
import { X } from "lucide-react";

import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { DataTableViewOptions } from "./data-table-view-options";

import { channels, priorities, reviewTypes, statuses } from "@/lib/data/data";
import { DataTableFacetedFilter } from "./data-table-faceted-filter";

interface DataTableToolbarProps<TData> {
  table: Table<TData>;
}

export function DataTableToolbar<TData>({
  table,
}: DataTableToolbarProps<TData>) {
  const isFiltered = table.getState().columnFilters.length > 0;

  return (
    <div className="flex items-center justify-between">
      <div className="flex flex-1 items-center gap-2">
        <Input
          placeholder="Filter properties..."
          value={
            (table.getColumn("listingName")?.getFilterValue() as string) ?? ""
          }
          onChange={(event) =>
            table.getColumn("listingName")?.setFilterValue(event.target.value)
          }
          className="h-8 w-[150px] lg:w-[250px]"
        />
        {table.getColumn("channel") && (
          <DataTableFacetedFilter
            column={table.getColumn("channel")}
            title="Channel"
            options={channels}
          />
        )}
        {table.getColumn("reviewType") && (
          <DataTableFacetedFilter
            column={table.getColumn("reviewType")}
            title="Review Type"
            options={reviewTypes}
          />
        )}
        {isFiltered && (
          <Button
            variant="ghost"
            size="sm"
            onClick={() => table.resetColumnFilters()}
          >
            Reset
            <X />
          </Button>
        )}
      </div>
      <div className="flex items-center gap-2">
        <DataTableViewOptions table={table} />
      </div>
    </div>
  );
}
